import UIKit

extension UIColor {
    
    // MARK: - Pink
    
    static var pink900: UIColor {
        return UIColor(hex: "#E31B54")
    }
    
    static var green400: UIColor {
        return UIColor(hex: "#23B877")
    }
    
    static var green200: UIColor {
        return UIColor(hex: "#59D7A0")
    }
    
    static var green100: UIColor {
        return UIColor(hex: "#9BE6C5")
    }
    
    static var green50: UIColor {
        return UIColor(hex: "23B877").withAlphaComponent(0.5)
    }
}
